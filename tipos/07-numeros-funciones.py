"""Funciones Nativas de Python para Números"""

import math  # Modulo para poder tener mas funciones matematicas con números

print(round(1.3))
print(round(1.7))
print(round(1.5))
print(abs(-77))  # Valor absoluto del número
print(abs(-55))  # Valor absoluto del número

print(math.ceil(1.1))  # Redondea hacia arriba
print(math.floor(1.9999))  # Redondea hacia abajo
print(math.isnan(23))  # Verifica si el número es un número
print(math.pow(5, 3))  # Potencia
print(math.sqrt(25))  # Raiz cuadrada
